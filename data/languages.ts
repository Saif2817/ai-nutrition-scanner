export const getLanguageRecommendations = (state: string, city: string): string[] => {
  const stateData = indianLanguagesData[state];
  if (!stateData) return ["English", "Hindi"];

  let recommendations: string[] = [];

  // City-specific recommendations first
  if (city && stateData.cities[city]) {
    recommendations = [...stateData.cities[city]];
  } else {
    recommendations = [...stateData.primary];
  }

  // Add secondary languages, ensuring no duplicates
  stateData.secondary.forEach((lang: string) => {
    if (!recommendations.includes(lang)) {
      recommendations.push(lang);
    }
  });

  return recommendations;
};

export const getAllStates = (): string[] => {
    return Object.keys(indianLanguagesData).sort();
}

export const getCitiesForState = (state: string): string[] => {
    if (indianLanguagesData[state] && indianLanguagesData[state].districts) {
        return indianLanguagesData[state].districts.sort();
    }
    return [];
}

export const getAllLanguages = (): string[] => {
    const allLangs = new Set<string>();
    Object.values(indianLanguagesData).forEach(state => {
        state.primary.forEach((l: string) => allLangs.add(l));
        state.secondary.forEach((l: string) => allLangs.add(l));
        Object.values(state.cities).forEach((cityLangs: any) => {
            cityLangs.forEach((l: string) => allLangs.add(l));
        });
    });
    return Array.from(allLangs).sort();
}